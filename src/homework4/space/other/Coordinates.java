package homework4.space.other;

public class Coordinates {

	private final double lineX;
	private final double lineY;

	public Coordinates(double lineX, double lineY) {
		this.lineX = lineX;
		this.lineY = lineY;
	}

	public double getLineX() {
		return lineX;
	}

	public double getLineY() {
		return lineY;
	}
}
